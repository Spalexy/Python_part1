# Задание-1:
# Дан список, заполненный произвольными целыми числами. 
# Получить новый список, элементы которого будут
# квадратами элементов исходного списка
# [1, 2, 4, 0] --> [1, 4, 16, 0]

list_1 = [1, 2, 3, 8, 24, 11, -1]


list_2 = (i ** 2 for i in list_1) 


# Задание-2:
# Даны два списка фруктов. Получить список фруктов, присутствующих в обоих исходных списках.

list_1 = ['яблоко', 'ананас', 'груша', 'персик', 'апельсин', 'абрикос', 'мандарин']
list_2 = ['мандарин', 'груша', 'банан', 'киви', 'нектарин', 'ананас']

